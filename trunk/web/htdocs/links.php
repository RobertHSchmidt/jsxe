      <h2>Getting Involved</h2>
        
      <p>Getting involved is fairly easy. There are a multitude of ways that
          you can become involved in the project. The easiest is probably by
          subscribing to the mailing lists, however there are many methods at
          your disposal.</p>
      
      <h3>Mailing Lists</h3>
      
      <p>These three mailing lists will be low traffic.</p>
      
      <ul>
        <li>jsXe-users: <a href="http://lists.sourceforge.net/mailman/listinfo/jsxe-users">Subscribe/Unsubscribe/Preferences</a>: a general discussion list for users of jsXe.</li>
        <li>jsXe-devel: <a href="http://lists.sourceforge.net/mailman/listinfo/jsxe-devel">Subscribe/Unsubscribe/Preferences</a>: a list for technical discussion regarding jsXe development.</li>
        <li>jsXe-cvs: <a href="http://lists.sourceforge.net/mailman/listinfo/jsxe-cvs">Subscribe/Unsubscribe/Preferences</a>: a list where cvs commits are posted. Anyone subscribing to this list will be notified when changes are commited to cvs.</li>
      </ul>
      
      <h3>Forums</h3>
      
      <p>To get support if you&apos;re having problems installing or running
         jsXe please post to the Help forum. Any other comments and criticism
         can be posted in- the open discussion forum.</p>
      <ul>
        <li><a href="http://sourceforge.net/forum/forum.php?forum_id=197160">Open Discussion</a></li>
        <li><a href="http://sourceforge.net/forum/forum.php?forum_id=197161">Help</a></li>
      </ul>
      
      <h3>IRC</h3>

      <p>Come to #jsxe on irc.freenode.net to talk with the developers.</p>
      
      <h3>Reporting Bugs</h3>
      
      <p>Reporting bugs is easy. Just submit a bug report on <a href="http://sourceforge.net/tracker/?atid=488268&amp;group_id=58584&amp;func=browse">jsXe&apos;s Bug Tracker</a>.
         Just enter a description of the bug and if possible how to reproduce it
         and it will be assigned and addressed by the project development team.</p>
         
      <h3>Feature Requests</h3>
      
      <p>Features requests are always welcome and will be addressed based on user
         need and whether the feature fits with jsXe&apos;s vision. Feature
         requests are easy too! Just submit a feature requests to
         <a href="http://sourceforge.net/tracker/?atid=488271&amp;group_id=58584&amp;func=browse">jsXe&apos;s Feature Request Tracker</a>.</p>
      
      <h3>Patches</h3>
      
      <p>This is probably the best way to start becoming involved in the devolopment of jsXe.
         You can submit patches using 
         <a href="http://sourceforge.net/tracker/?atid=488270&amp;group_id=58584&amp;func=browse">jsXe&apos;s Sourceforge Patch Tracker</a>.
         Just provide the patch or code change itself and a discription of what
         it does when you submit it. If your work is good then we&apos;ll add
         you as developer with cvs access.</p>
