<h3>Reporting Bugs</h3>

<p>Reporting bugs is easy. Just submit a bug report on <a href="http://sourceforge.net/tracker/?atid=488268&amp;group_id=58584&amp;func=browse">jsXe&apos;s Bug Tracker</a>.
   Just enter a description of the bug and if possible how to reproduce it
   and it will be assigned and addressed by the project development team.</p>
 
<h3>Feature Requests</h3>

<p>Features requests are always welcome and will be addressed based on user
   need and whether the feature fits with jsXe&apos;s vision. Feature
   requests are easy too! Just submit a feature requests to
   <a href="http://sourceforge.net/tracker/?atid=488271&amp;group_id=58584&amp;func=browse">jsXe&apos;s Feature Request Tracker</a>.</p>

<h3>Patches</h3>

<p>This is probably the best way to start becoming involved in the devolopment of jsXe.
   You can submit patches using 
   <a href="http://sourceforge.net/tracker/?atid=488270&amp;group_id=58584&amp;func=browse">jsXe&apos;s Sourceforge Patch Tracker</a>.
   Just provide the patch or code change itself and a discription of what
   it does when you submit it. If your work is good then we&apos;ll add
   you as developer with SVN access.</p>