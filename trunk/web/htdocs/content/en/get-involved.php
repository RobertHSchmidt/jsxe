<p>jsXe needs developers to implement new features and fix bugs,
   testers for testing jsXe, translators for translating messages and the
   website, and web designers who are interested in updating the project's
   website.</p>

<p>Getting involved is fairly easy. There are a multitude of ways that
   you can become involved in the project. The easiest is probably by
   subscribing to the mailing lists, however there are many methods at
   your disposal.</p>
 
<p>Note: Contributers will be expected to give their real names.</p> 

<h3>Developers</h3>

<p>If you are interested in working on jsXe, please
   <a href="#lists">subscribe</a>
   and send a mail to the jsxe-devel mailing list. The developers can help
   get you started. Developers should know how to use SVN and have some
   experience with Java swing or XML.</p>

<h3>Testers</h3>

<p>If you are interested in testing jsXe, please
   <a href="#lists">subscribe</a>
   and send a mail to the jsxe-devel mailing list. Testing will normally be
   done on SVN and beta releases. Familiarity with 
   <a href="http://subversion.tigris.org/">SVN</a> and 
   <a href="http://ant.apache.org/">ant</a> is a plus since it will be needed
   for testing code from the SVN repository.</p>

<h3>Translators</h3>

<p>If you are interested in translating for the jsXe project. Please
   <a href="#lists">subscribe</a>
   and send a mail to the jsxe-devel mailing list. For those who are
   knowlegeable of SVN, you can check out the messages files from jsXe's 
   <a href="http://svn.sourceforge.net/viewcvs.cgi/jsxe/trunk/jsxe/">SVN repository</a>.
   But even if you are not versed in SVN, the developers on the
   mailing list can help get you started.</p>

<h3>Web Developers</h3>

<p>If you are interested in working on jsXe's webpage, please
   <a href="#lists">subscribe</a>
   and send a mail to the jsxe-devel mailing list. The developers can help
   get you started. Web developers should have some knowledge of CSS and
   PHP.</p>

<h3>Users</h3>

<p>Let us know what you think!
   <a href="#lists">Subscribe</a>
   to the jsxe-users mailing list and let us know what you think and what
   features you would like to have implemented. Or you could submit a 
   <a href="http://sourceforge.net/tracker/?atid=488271&amp;group_id=58584&amp;func=browse">feature request</a>.</p>

<h3><a name="lists">Mailing Lists</a></h3>

