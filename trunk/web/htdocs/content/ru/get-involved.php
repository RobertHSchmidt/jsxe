<h3>IRC</h3>

<p>Посетите #jsxe на irc.freenode.net чтобы поговорить с разработчиками.</p>

<h3>Если вы нашли ошибку (баг).</h3>

<p>Сообщить о баге просто. Достаточно оставить багрепорт в <a href="http://sourceforge.net/tracker/?atid=488268&amp;group_id=58584&amp;func=browse">в баг-трекере jsXe</a>.
   Оставьте описание ошибки, и если возможно - то способ, которым можно
   эту ошибку специально вызвать. Команда разработчиков получит эту информацию.</p>

<h3>Предложения (Feature Requests)</h3>

<p>Ваши предложения всегда приветствуются и будут приняты в зависимости от
   того насколько это необходимо пользователям, а также на основании того
   насколько предложение укладывается в представления о том каким должен быть
   jsXe. Отправить предложение не сложнее чем сообщение о ошибке.
   Сделать это можно в
   <a href="http://sourceforge.net/tracker/?atid=488271&amp;group_id=58584&amp;func=browse">трекере предложений jsXe</a>.</p>

<h3>Патчи</h3>

<p>Это лучший способ чтобы начать помогать в разработке jsXe.
   Вы можете отправлять патчи с помощью
   <a href="http://sourceforge.net/tracker/?atid=488270&amp;group_id=58584&amp;func=browse">трекера патчей jsXe</a>.
   Просто предоставьте патч или изменение кода + описание того, что ваш патч
   делает и отправьте. Если ваша работа хороша, то мы дадим вам
   доступ разработчика к нашему SVN.</p>
