  <li>jsXe-devel (English): <a href="http://lists.sourceforge.net/mailman/listinfo/jsxe-devel">Subscribe/Unsubscribe/Preferences</a>: обсуждение технических вопросов о разработке jsXe.</li>
  <li>jsXe-users (日本語): <a href="http://lists.sourceforge.jp/mailman/listinfo/jsxe-users">Subscribe/Unsubscribe/Preferences</a>: основной список рассылки для пользователей jsXe.</li>
