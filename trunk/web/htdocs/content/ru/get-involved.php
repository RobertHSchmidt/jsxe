<p>Проекту jsXe нужны разработчики для реализации новых возможностей и
   охоты за жуками, тестеры для (как это ни странно) тестирования jsXe,
   переводчики для того чтобы переводить сообщения внутри программы и тексты
   на сайте проекта, вебдизайнеры которые заинтересованы в обновлении сайта
   проекта.</p>

<p>Принять участие довольно просто. Существует несколько способов. Наиболее
   простой - подписаться на списки рассылки, однако в вашем распоряжении
   есть несколько методов.</p>

<p>Обратите внимание: участникам проекта требуется указать свои настоящие имена.</p>

<h3>Разработчики</h3>

<p>Если вы заинтересованы в работе над jsXe, просьба
   <a href="#lists">подписаться</a>
   и отправить письмо в список рассылки jsxe-devel. Другие разработчики
   могут помочь вам начать. Разработчику необходимо знать как использовать
   SVN и иметь некоторый опыт в работе с Java или XML.</p>

<h3>Тестеры</h3>

<p>Если вам интересно тестирование jsXe, просьба
   <a href="#lists">подписаться</a>
   и отправить письмо в список рассылки jsxe-devel. Обычно тестируются сборки
   из SVN и бета-релизы.
   done on SVN and beta releases. Знание
   <a href="http://subversion.tigris.org/">SVN</a> и
   <a href="http://ant.apache.org/">ant</a> желательно, поскольку оно потребуется
   для тестирования кода из репозитория SVN.</p>

<h3>Переводчики</h3>

<p>Если вы заинтересованы в переводе jsXe на другие языки, просьба
   <a href="#lists">подписаться</a>
   и отправить письмо в список рассылки jsxe-devel. Для тех кто знаком
   с SVN - вы можете чекаутить message-файлы из
   <a href="http://svn.sourceforge.net/viewcvs.cgi/jsxe/trunk/jsxe/">SVN-репозитория</a>
   jsXe. Но если у вас нет опыта работы с SVN, в списках рассылки
   разработчики могут помочь вам.</p>

<h3>Web-разработчики</h3>

<p>Если вы заинтересованы в работе над страницей jsXe, просьба
   <a href="#lists">подписаться</a>
   и отправить письмо в список рассылки jsxe-devel. Разработчики могут помочь
   вам начать. Вам отребуются знание CSS и PHP.</p>

<h3>Пользователи</h3>

<p>Дайте нас знать что вы думаете о программе!
   <a href="#lists">Подпишитесь</a>
   на список рассылки jsxe-users и присылайте туда ваши предложения о том
   какие возможности вы хотели бы видеть реализованными в новых версиях jsXe.
   Вы также можете добавить новый
   <a href="http://sourceforge.net/tracker/?atid=488271&amp;group_id=58584&amp;func=browse">feature request</a>
   - это предложение о изменении чего-либо в последующих версиях, для каждого feature request
   создается отдельная страница и на ней можно будет отслеживать реакцию разработчиков на
   ваше предложение.</p>

<h3><a name="lists">Списки рассылки</a></h3>

