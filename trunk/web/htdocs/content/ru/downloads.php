<h3>Требования</h3>

<ul>
<li>jsXe требует Java 2 версии 1.4.2</li>
<li>jsXe в настоящее время требуется особая версия Xerces-J для
    запуска. Xerces-J 2.8.0 необходим с тех пор, как некоторые
    возможности были реализованы с применением DOM level 3
    interfaces которые были в версии 2.6.0, но затем были изменены
    в более поздних версиях. Xerces-J 2.8.0 входит в состав
    обычных релизов jsXe (и бинарники и исходники) для удобства.
    Вы также можете найти Xerces-J 2.8.0
    <a href="http://archive.apache.org/dist/xml/xerces-j/">здесь</a>.</li>
</ul>

